package nasmsg

import (
	"bytes"
	"fmt"
	"testing"
)

func TestRegistrationRequestMsg_Decode(t *testing.T) {
	request := []byte{0x79, 0x00, 0x0d, 0x01, 0x64, 0xf0, 0x10, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x50, 0x10, 0x01, 0x03, 0x2e, 0x02,
		0xf0, 0xf0, 0x17, 0x07, 0xf0, 0xf0, 0xc0, 0xc0, 0x01, 0x80, 0x30}

	msgbuf := bytes.NewReader(request)

	rrmsg := RegistrationRequestMsg{}
	rrmsg.Reset()

	err := rrmsg.Decode(msgbuf)
	if err != nil {
		t.Errorf("failed to decode msg")
	}

	fmt.Println(rrmsg.String())
}
