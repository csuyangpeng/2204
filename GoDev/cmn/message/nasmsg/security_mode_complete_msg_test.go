package nasmsg

import (
	"bytes"
	"fmt"
	"testing"
)

func TestSecurityModeCompleteMsg_Decode(t *testing.T) {
	nasmsg := []byte{0x77, 0x00, 0x09, 0x15, 0x32, 0x54, 0x76, 0x98, 0x68, 0x45, 0x23, 0xf1, 0x71, 0x00, 0x1a, 0x7e, 0x00, 0x41, 0x79, 0x00, 0x0d, 0x01, 0x13, 0x91, 0x84, 0x00,
		0x00, 0x00, 0x00, 0x00, 0x30, 0x15, 0x41, 0xf1, 0x10, 0x01, 0x01, 0x2e, 0x02, 0x80, 0x80}

	msgbuf := bytes.NewReader(nasmsg)

	secModCmp := SecurityModeCompleteMsg{}
	secModCmp.Reset()

	err := secModCmp.Decode(msgbuf)
	if err != nil {
		t.Errorf("failed to decode msg")
	}

	fmt.Println(secModCmp.String())
	//2950986c08806db6794ce9349ed36e7c
}
